	  <h2>Background</h2>		

			<p>On the 23 rd of May, 2016, a group of dedicated ECE practitioners came together with Prof. David Galloway, Durham UK to hold a conference for teachers, leaders and policy makers on the importance of quality standard in the sector. The team, having international experience and local practice, came to realize that in 20 years, with the changes in education reforms and many policy documents entrenched in the sector, education lacked the impact envisioned. As most young people having gone through the education system in Sri Lanka, still lacked the skills and competencies necessary to be successful as adults. Throughout the conference, it was apparent that there is still more work to be done in the sector of education to improve the existing education systems that is fragmented, needing standardization and qualified staff. The birth of the foundation was brought forth by the team, deliberating the problems at hand and creating Global Foundation for Quality Education.
